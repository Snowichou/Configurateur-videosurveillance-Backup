// Détection langue navigateur → fallback FR
let _currentLang = "fr";
(function detectLang() {
  const nav = (navigator.language || navigator.userLanguage || "fr").slice(0, 2).toLowerCase();
  _currentLang = TRANSLATIONS[nav] ? nav : "fr";
})();

// Fonction de traduction
export function T(key) {
  return (TRANSLATIONS[_currentLang] && TRANSLATIONS[_currentLang][key])
    || (TRANSLATIONS.fr && TRANSLATIONS.fr[key])
    || key;
}

// Changer de langue
export function setLang(code) {
  if (!TRANSLATIONS[code]) return;
  _currentLang = code;
  localStorage.setItem("comelit_lang", code);
  // Forcer un rechargement complet pour appliquer la langue partout
  window.location.reload();
}

// Charger la langue sauvegardée
(function loadSavedLang() {
  const saved = localStorage.getItem("comelit_lang");
  if (saved && TRANSLATIONS[saved]) _currentLang = saved;
})();

// Mettre à jour les labels du stepper (dans le DOM initial)
export function updateStepperLabels() {
  const keys = ["step_project", "step_cameras", "step_mounts", "step_storage", "step_nvr", "step_options", "step_summary"];
  const labels = document.querySelectorAll(".stepperLabel");
  labels.forEach((el, i) => {
    if (keys[i]) el.textContent = T(keys[i]);
  });
}

// Mettre à jour les boutons de navigation
export function updateNavButtons() {
  const btnPrev = document.getElementById("btnPrev");
  const btnReset = document.getElementById("btnReset");
  const btnDemo = document.getElementById("btnDemo");
  if (btnPrev) {
    const lbl = btnPrev.querySelector(".btnLabel");
    if (lbl) lbl.textContent = T("btn_prev");
  }
  if (btnReset) btnReset.textContent = T("btn_reset");
  if (btnDemo) btnDemo.textContent = T("btn_demo");
}

// Sélecteur de langue (HTML à injecter dans le header)
export function getLangSelectorHtml() {
  const langs = Object.keys(TRANSLATIONS);
  return '<select id="langSelector" class="langSelect" title="' + T("lang_label") + '">'
    + langs.map(code => {
      const t = TRANSLATIONS[code];
      return '<option value="' + code + '"' + (_currentLang === code ? ' selected' : '') + '>' + t._flag + ' ' + t._name + '</option>';
    }).join("")
    + '</select>';
}

// Exposer globalement
window.TRANSLATIONS = TRANSLATIONS;
window.T = T;
window._currentLang = _currentLang;
